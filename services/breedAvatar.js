/**
 * 取 breed 对应 emoji；找不到时按 petType 返回对应默认
 * @param {string} breed
 * @param {"dog"|"cat"|string} [petType="dog"]
 */
export function avatarForBreed(breed, petType = "dog") {
  if (!breed) return petType === "cat" ? "🐱" : "🐾";
  return BREED_EMOJI[breed] || (petType === "cat" ? "🐱" : "🐾");
}

/**
 * 取 pet 对象的 emoji
 */
export function avatarForPet(pet) {
  return avatarForBreed(pet?.breed, pet?.pet_type);
}
